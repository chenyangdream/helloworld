#!/usr/bin/pyenv

class Student(object):

    def __init__():
        self.no = None
        self.name = ''

    def print():
        print("I am a Student.")

class MiddleSchoolStudent(Student):
    def __init__():
        self.no = None
        self.name = ''
    
    def print():
        print("I am a Middle School Student.")

class HighSchoolStudent(Student):
    def __init__():
        self.no = None
        self.name = ''
    
    def print():
        print("I am a High School Student.")
